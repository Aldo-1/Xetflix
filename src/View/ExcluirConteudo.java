    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String nomeDoConteudoExcluir = this.nomeDoConteudo.getText();
        if (this.checkFilme.isSelected()) {
            Filme filmeRemover = this.netflixBancoDeDados.buscarFilme(nomeDoConteudoExcluir);
            if (filmeRemover != null) {
                int indice = this.netflixBancoDeDados.getFilmes().indexOf(filmeRemover);
                netflixBancoDeDados.getFilmes().get(indice).setDiretor(null);
                JOptionPane.showMessageDialog(null, "Diretor removido com sucesso");
            }
        } else {
            Serie serieRemover = this.netflixBancoDeDados.buscarSerie(nomeDoConteudoExcluir);
            if (serieRemover != null) {
                int indice = this.netflixBancoDeDados.getSeries().indexOf(nomeDoConteudoExcluir);
                netflixBancoDeDados.getSeries().get(indice).setDiretor(null);
                JOptionPane.showMessageDialog(null, "Diretor removido com sucesso");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

