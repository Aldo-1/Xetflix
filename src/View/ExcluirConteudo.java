    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String nomeDoConteudoExcluir = this.nomeDoConteudo.getText();
        String nomeDoAtorExcluir = this.txtAtor.getText();
        if (this.checkFilme.isSelected()) {
            Filme filmeRemover = this.netflixBancoDeDados.buscarFilme(nomeDoConteudoExcluir);
            if (filmeRemover != null) {
                for (int i = 0; i < filmeRemover.getListaDeAtores().length; i++) {
                    if (filmeRemover.getListaDeAtores()[i].equals(nomeDoAtorExcluir)) {
                        filmeRemover.getListaDeAtores()[i] = null;
                        filmeRemover.getListaDeAtores()[i] = "";
                    }

                }
                JOptionPane.showMessageDialog(null, "Ator removido com sucesso");
            }
        } else {
            Serie serieRemover = this.netflixBancoDeDados.buscarSerie(nomeDoConteudoExcluir);

            if (serieRemover != null) {
                for (int i = 0; i < serieRemover.getListaDeAtores().length; i++) {
                    if (serieRemover.getListaDeAtores()[i].equals(nomeDoAtorExcluir)) {
                        serieRemover.getListaDeAtores()[i] = null;
                        serieRemover.getListaDeAtores()[i] = "";
                    }
                }
                JOptionPane.showMessageDialog(null, "Ator removido com sucesso");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

