    private ImageIcon redimensionar(JButton botao, int xLargura, int yAltura) {
        ImageIcon img = new ImageIcon(botao.getIcon().toString());
        img.setImage(img.getImage().getScaledInstance(xLargura, yAltura, 100));
        return img;
    }

